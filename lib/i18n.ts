export function translate(
  lang: Lang,
  key: string,
  vars?: Record<string, string | number>
): string {
  let s = dict[lang][key] ?? dict.zh[key] ?? key;
  if (vars) {
    for (const k of Object.keys(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(vars[k]));
    }
  }
  return s;
}
